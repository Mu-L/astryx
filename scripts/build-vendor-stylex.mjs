/**
 * @file build-vendor-stylex.mjs
 * Post-build step that makes @astryxdesign/core self-contained at RUNTIME without
 * forcing consumers to install @stylexjs/stylex.
 *
 * @stylexjs/stylex is declared as an OPTIONAL peerDependency. Compiled components
 * call stylex.props() (plus stylex.when/stylex.create) at runtime, so the runtime
 * must be present even when the peer is not installed. This script:
 *   1. Bundles the StyleX *runtime* (its ESM entry + transitive deps) into a single
 *      self-contained ESM module at dist/_vendor/stylex.js.
 *   2. Rewrites every `import * as stylex from '@stylexjs/stylex'` in dist/**\/*.js
 *      to a relative import of that vendored module.
 *
 * TYPES are intentionally NOT vendored: the emitted .d.ts keep importing types from
 * '@stylexjs/stylex'. StyleX brands its types with `unique symbol`s, so a *copied*
 * type would be nominally incompatible with a consumer's real StyleX value at the
 * public `xstyle` prop (TS2322 — verified). Keeping the type import pointed at the
 * optional peer means a single symbol identity, hence no conflict. Consumers who use
 * `xstyle` already depend on @stylexjs/stylex to author the value, so requiring the
 * peer for those types costs them nothing.
 *
 * Runs after build:esm + tsc (declaration-only) and before build:umd.
 *
 * Usage: node scripts/build-vendor-stylex.mjs
 */

import {build} from 'esbuild';
import {glob} from 'glob';
import fs from 'fs/promises';
import path from 'path';
import {fileURLToPath} from 'url';

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const ROOT = path.resolve(__dirname, '..');
const CORE = path.resolve(ROOT, 'packages/core');
const DIST = path.resolve(CORE, 'dist');
const VENDOR_DIR = path.resolve(DIST, '_vendor');
const VENDOR_FILE = path.resolve(VENDOR_DIR, 'stylex.js');
const SPECIFIER = '@stylexjs/stylex';

/**
 * Bundle the StyleX runtime into a single self-contained ESM module.
 * `export *` re-exports all named runtime members (props, create, when, ...);
 * StyleX has no default export, so a namespace import keeps working unchanged.
 */
async function bundleRuntime() {
  await fs.mkdir(VENDOR_DIR, {recursive: true});
  await build({
    stdin: {
      contents: `export * from '${SPECIFIER}';`,
      resolveDir: CORE,
      sourcefile: 'vendor-stylex-entry.js',
      loader: 'js',
    },
    outfile: VENDOR_FILE,
    bundle: true,
    format: 'esm',
    platform: 'browser',
    target: ['es2020'],
    legalComments: 'none',
    banner: {
      js: '/* Vendored @stylexjs/stylex runtime. Generated by scripts/build-vendor-stylex.mjs. Do not edit. */',
    },
  });
}

/**
 * Rewrite bare `@stylexjs/stylex` runtime imports in the compiled output to a
 * relative import of the vendored module. Only touches .js (not .d.ts — types stay
 * pointed at the optional peer). Skips the vendored file and the UMD bundle.
 */
async function rewriteImports() {
  const files = await glob('**/*.js', {
    cwd: DIST,
    absolute: true,
    ignore: ['_vendor/**', 'astryx.umd.js'],
  });
  // from '@stylexjs/stylex' | from "@stylexjs/stylex"
  const re = new RegExp(`(from\\s*)(['"])${SPECIFIER.replace(/[/\\]/g, '\\$&')}\\2`, 'g');
  let changedFiles = 0;
  let occurrences = 0;
  for (const file of files) {
    const code = await fs.readFile(file, 'utf8');
    if (!code.includes(SPECIFIER)) continue;
    let rel = path.relative(path.dirname(file), VENDOR_FILE).split(path.sep).join('/');
    if (!rel.startsWith('.')) rel = `./${rel}`;
    const next = code.replace(re, (_m, fromKw, quote) => {
      occurrences++;
      return `${fromKw}${quote}${rel}${quote}`;
    });
    if (next !== code) {
      await fs.writeFile(file, next);
      changedFiles++;
    }
  }
  return {changedFiles, occurrences};
}

async function main() {
  await bundleRuntime();
  const {size} = await fs.stat(VENDOR_FILE);
  console.log(
    `Vendored StyleX runtime -> dist/_vendor/stylex.js (${(size / 1024).toFixed(1)} KB)`,
  );
  const {changedFiles, occurrences} = await rewriteImports();
  console.log(
    `Rewrote ${occurrences} '${SPECIFIER}' runtime import(s) across ${changedFiles} file(s) -> ./_vendor/stylex.js`,
  );

  // Safety: no bare runtime imports of the peer should remain in compiled JS.
  const leftover = await glob('**/*.js', {
    cwd: DIST,
    absolute: true,
    ignore: ['_vendor/**'],
  });
  const offenders = [];
  for (const file of leftover) {
    const code = await fs.readFile(file, 'utf8');
    if (new RegExp(`from\\s*['"]${SPECIFIER.replace(/[/\\]/g, '\\$&')}['"]`).test(code)) {
      offenders.push(path.relative(DIST, file));
    }
  }
  if (offenders.length) {
    console.error(
      `ERROR: bare '${SPECIFIER}' imports remain in: ${offenders.join(', ')}`,
    );
    process.exit(1);
  }
}

main().catch(err => {
  console.error(err);
  process.exit(1);
});
